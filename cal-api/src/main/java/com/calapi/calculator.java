package com.calapi;

public class calculator {
    public static int add(int first_num, int second_num) {
        int addition = first_num + second_num;
        return addition;
    }

    public static int subtract(int first_num, int second_num) {
        int subtraction = first_num - second_num;
        return subtraction;
    }

    public static int multiply(int first_num, int second_num) {
        int multiplication = first_num * second_num;
        return multiplication;
    }

    public static int divide(int first_num, int second_num) {
        int division = first_num / second_num;
        return division;
    }
}
